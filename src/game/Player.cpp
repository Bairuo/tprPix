/* ===========================================================
 *                    d2m    
 * -----------------------------------------------------------
 * -- 通过一个 diskPlayer 来改写 自己的各字段
 */
void Player::d2m( diskPlayer *_dp ){

    go.id = _dp->go_id;
    go.pos.x = (float)_dp->pos.x;
    go.pos.y = (float)_dp->pos.y;

}


/* ===========================================================
 *                      m2d  
 * -----------------------------------------------------------
 */
diskPlayer Player::m2d(){

    diskPlayer dp; //- return

    dp.go_id = go.id;
    dp.pos.x = (int)go.pos.x;
    dp.pos.y = (int)go.pos.y;


    return dp;
}


