#include "Move.h"

//-------------------- Engine --------------------//
#include "GameObj.h" 


/* ===========================================================
 *                        init
 * -----------------------------------------------------------
 */
void Move::init( GameObj *_goPtr ){
    goPtr = _goPtr;
    //...
    crawl.init( goPtr, (Move*)this ); 
    fly.init(   goPtr, (Move*)this );
}


/* ===========================================================
 *                   RenderUpdate   
 * -----------------------------------------------------------
 */
void Move::RenderUpdate(){

    if( is_crawl_ == true ){
        crawl.RenderUpdate();
    }else{
        fly.RenderUpdate();
    }
}


